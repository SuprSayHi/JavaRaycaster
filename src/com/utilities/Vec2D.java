package com.utilities;

public class Vec2D {
    public double x, y;


    // Constructors
    public Vec2D() {
        this.x = 0;
        this.y = 0;
    }

    public Vec2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Vec2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    
}
